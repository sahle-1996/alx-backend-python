#!/usr/bin/env python3
"""
A script defining a type-annotated function 'to_str' that takes
a float as input and returns its string representation.
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string form.

    Args:
        n (float): The number to convert.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
